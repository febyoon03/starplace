def _hour_ji_idx(hour:int) -> int:
    if hour in (23, 0): return 0
    mapping = [(1,3,1),(3,5,2),(5,7,3),(7,9,4),(9,11,5),(11,13,6),
               (13,15,7),(15,17,8),(17,19,9),(19,21,10),(21,23,11)]
    for s,e,i in mapping:
        if s <= hour < e: return i
    return 0

@st.cache_data
def calculate_ziwei(month:int, hour:int) -> dict:
    """자미두수 명궁 계산 (캐싱 — 생월+생시 조합 재사용)"""
    ji_idx = MINGONG_TABLE[month-1][_hour_ji_idx(hour)]
    ji = JIJI[ji_idx]
    p = PROFILES[ji]
    return {
        "명궁지지": ji,
        "주성": p["주성"],
        "성격": p["성격"],
        "강점": p["강점"],
        "보정": p["보정"],
    }

def apply_ziwei_correction(ohaeng:dict, ziwei:dict) -> dict:
    corrected = {k: v + ziwei["보정"].get(k, 0) for k,v in ohaeng.items()}
    total = sum(corrected.values()) or 1
    return {k: round(v/total, 3) for k,v in corrected.items()}
